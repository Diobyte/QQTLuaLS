---@meta

---@see https://github.com/qqtnn/qqt_diablo.wiki.git/wiki/Pathfinder

---@class pathfinder

local pathfinder = {}

---@param pos vec3
---@return vec3[]
---@description Generates a path to the specified position using the game engine's pathfinding functionality. While accurate, it's resource-intensive and best used sparingly. It's recommended to store the path for repeated use rather than generating it multiple times.
---@example local path = pathfinder.create_path_game_engine(target_pos)
function pathfinder.create_path_game_engine(pos) end

---@param pos vec3
---@param waypoint_list vec3[]
---@param threshold number
---@return vec3
---@description Identifies the next waypoint in a list, ideal for navigating paths generated by `create_path_game_engine`. When used in conjunction with `request_move`, it streamlines movement by avoiding unnecessary move orders.
---@example local next_wp = pathfinder.get_next_waypoint(current_pos, path, 1.0)
function pathfinder.get_next_waypoint(pos, waypoint_list, threshold) end

---@param pos vec3
---@return nil
---@description Instantly moves the player character to the specified position, overriding any current movement or actions. Useful for urgent or precise movement requirements.
---@example pathfinder.force_move(vec3.new(100, 0, 100))
function pathfinder.force_move(pos) end

---@param pos vec3
---@return nil
---@description Submits a movement request to the player character. Unlike `force_move`, it only sends a command if the player isn't already moving, optimizing movement and reducing command redundancy.
---@example pathfinder.request_move(target_position)
function pathfinder.request_move(pos) end

---@param start vec3
---@param goal vec3
---@return vec3[]
---@description Returns a table of `vec3` waypoints representing the calculated path from start to goal.
---@example local waypoints = pathfinder.calculate_and_get_path_points(start_pos, goal_pos)
function pathfinder.calculate_and_get_path_points(start, goal) end

---@param pos vec3
---@param efficient boolean
---@return nil
---@description This function is best used for short to medium distance movements, utilizing the orbwalker's integrated pathfinder. For longer distances, consider using the game engine's pathfinder. The `efficient` parameter allows for quicker, less resource-intensive path calculations.
---@example pathfinder.move_to_cpathfinder(target, true)
function pathfinder.move_to_cpathfinder(pos, efficient) end

---@param pos vec3
---@param algo_type number
---@param batch_length number
---@param circle_rad number
---@param circular_precision number
---@param max_algo_steps number
---@param anti_stuck_rad number
---@param anti_stuck_time number
---@return nil
---@description Advanced pathfinding with full customization parameters for algorithm type, batch processing, circular radius, precision, max steps, and anti-stuck mechanisms.
function pathfinder.move_to_cpathfinder_custom_full(pos, algo_type, batch_length, circle_rad, circular_precision,
                                                    max_algo_steps, anti_stuck_rad, anti_stuck_time)
end

---@param pos vec3
---@param batch_length number
---@param circle_rad number
---@param max_algo_steps number
---@param anti_stuck_rad? number
---@param anti_stuck_time? number
---@return nil
---@description Customized pathfinding with batch length, circle radius, max steps, and optional anti-stuck parameters.
function pathfinder.move_to_cpathfinder_custom_a1(pos, batch_length, circle_rad, max_algo_steps, anti_stuck_rad,
                                                  anti_stuck_time)
end

---@param pos vec3
---@param circular_precision number
---@param max_algo_steps number
---@param anti_stuck_rad? number
---@param anti_stuck_time? number
---@return nil
---@description Customized pathfinding focused on circular precision, max steps, and optional anti-stuck parameters.
function pathfinder.move_to_cpathfinder_custom_a2(pos, circular_precision, max_algo_steps, anti_stuck_rad,
                                                  anti_stuck_time)
end

---@return nil
---@description Clears the currently stored path in the pathfinder. This can be useful to reset or clear navigation paths.
function pathfinder.clear_stored_path() end

---@param pos vec3
---@return boolean
---@description Forces a movement to the specified position without considering the current pathfinding logic. This can be useful for direct movement commands.
function pathfinder.force_move_raw(pos) end

---@param waypoint_list table
---@param point vec3
---@return table
---@description Sorts the waypoint list based on proximity to the given point.
---@example local sorted = pathfinder.sort_waypoints(my_waypoints, my_pos)
function pathfinder.sort_waypoints(waypoint_list, point) end

---@param value number
---@return boolean indicating whether the movement command was successfully issued.
function pathfinder.set_last_waypoint_index(value) end
